package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'help' - отображает справочную информацию о доступных командах.
 * Выводит список всех команд с их описаниями.
 * Наследует базовую функциональность от абстрактного класса Command
 * и реализует интерфейс Serializable для поддержки сериализации.
 */
public class Help extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта при необходимости.
     * Значение 1341L выбрано как уникальный номер версии для этого класса.
     */
    @Serial
    private static final long serialVersionUID = 1341L;

    /**
     * Конструктор команды help.
     * Инициализирует команду с:
     * - именем "help"
     * - описанием "help : вывести справку по доступным командам"
     * - флагом false, указывающим что команда не требует дополнительных аргументов
     */
    public Help() {
        super("help",
                "help : вывести справку по доступным командам",
                false);
    }
}