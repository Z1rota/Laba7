package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'login' - обеспечивает аутентификацию пользователя.
 * Позволяет пользователю войти в систему под своей учетной записью.
 * Наследует базовую функциональность от абстрактного класса Command
 * и реализует интерфейс Serializable для поддержки сериализации.
 *
 * <p>Команда не требует дополнительных аргументов, данные для входа
 * запрашиваются в процессе выполнения.</p>
 */
public class Login extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 341L представляет собой уникальный номер версии для этого класса.
     */
    @Serial
    private static final long serialVersionUID = 341L;

    /**
     * Конструктор команды login.
     * Инициализирует команду с параметрами:
     * - имя команды: "login"
     * - описание: "Войти в аккаунт"
     * - флаг hasArgs: false (команда не требует аргументов в командной строке)
     */
    public Login() {
        super("login", "Войти в аккаунт", false);
    }
}