package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'info' - выводит метаинформацию о коллекции.
 * Отображает такие характеристики коллекции как:
 * - тип коллекции
 * - дата инициализации
 * - количество элементов
 * - и другие параметры
 * Наследует функциональность от абстрактного класса Command
 * и реализует интерфейс Serializable для поддержки сериализации.
 */
public class Info extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает совместимость между различными версиями класса при сериализации.
     * Значение 1342L выбрано как уникальный идентификатор для этого класса.
     */
    @Serial
    private static final long serialVersionUID = 1342L;

    /**
     * Конструктор команды info.
     * Инициализирует команду с параметрами:
     * - имя команды: "info"
     * - описание: вывод информации о коллекции
     * - флаг hasArgs: false (команда не требует дополнительных аргументов)
     */
    public Info() {
        super("info",
                "info : вывести в стандартный поток вывода информацию о коллекции " +
                        "(тип, дата инициализации, количество элементов и т.д.)",
                false);
    }
}