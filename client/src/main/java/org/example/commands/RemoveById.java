package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'remove_by_id' - удаляет элемент коллекции по указанному идентификатору.
 * Наследует функциональность от абстрактного класса Command и реализует интерфейс
 * Serializable для поддержки сериализации.
 *
 * <p>Команда требует обязательного аргумента - числового идентификатора элемента.</p>
 * <p>При выполнении проверяет существование элемента с указанным ID в коллекции.</p>
 */
public class RemoveById extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 1346L представляет собой уникальный номер версии для данного класса.
     */
    @Serial
    private static final long serialVersionUID = 1346L;

    /**
     * Конструктор команды remove_by_id.
     * Инициализирует команду с параметрами:
     * - имя команды: "remove_by_id"
     * - описание: удаление элемента по идентификатору
     * - флаг hasArgs: true (команда требует обязательного аргумента - ID элемента)
     */
    public RemoveById() {
        super("remove_by_id",
                "remove_by_id id : удалить элемент из коллекции по его id",
                true);
    }
}