package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'show' - отображает все элементы коллекции в строковом представлении.
 * Наследует функциональность от абстрактного класса Command и реализует
 * интерфейс Serializable для поддержки сериализации.
 *
 * <p>Выводит полное содержимое коллекции в стандартный поток вывода.</p>
 * <p>Если коллекция пуста, выводит соответствующее уведомление.</p>
 * <p>Каждый элемент отображается в удобочитаемом строковом формате.</p>
 */
public class Show extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 1348L представляет собой уникальный номер версии для данного класса.
     */
    @Serial
    private static final long serialVersionUID = 1348L;

    /**
     * Конструктор команды show.
     * Инициализирует команду с параметрами:
     * - имя команды: "show"
     * - описание: вывод всех элементов коллекции
     * - флаг hasArgs: false (команда не требует дополнительных аргументов)
     */
    public Show() {
        super("show",
                "show : вывести в стандартный поток вывода все элементы " +
                        "коллекции в строковом представлении",
                false);
    }
}