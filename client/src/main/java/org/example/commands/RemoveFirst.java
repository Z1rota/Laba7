package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'remove_first' - удаляет первый элемент из коллекции.
 * Наследует функциональность от абстрактного класса Command и реализует
 * интерфейс Serializable для поддержки сериализации.
 *
 * <p>Удаляет элемент, находящийся в начале коллекции, согласно текущему порядку элементов.</p>
 * <p>Если коллекция пуста, команда не выполняет никаких действий.</p>
 */
public class RemoveFirst extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 1347L представляет собой уникальный номер версии для данного класса.
     */
    @Serial
    private static final long serialVersionUID = 1347L;

    /**
     * Конструктор команды remove_first.
     * Инициализирует команду с параметрами:
     * - имя команды: "remove_first"
     * - описание: удаление первого элемента коллекции
     * - флаг hasArgs: false (команда не требует дополнительных аргументов)
     */
    public RemoveFirst() {
        super("remove_first",
                "remove_first : удалить первый элемент из коллекции",
                false);
    }
}