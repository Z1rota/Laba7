package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'shuffle' - перемешивает элементы коллекции в случайном порядке.
 * Наследует функциональность от абстрактного класса Command и реализует
 * интерфейс Serializable для поддержки сериализации.
 *
 * <p>Использует алгоритм Фишера-Йетса для равномерного распределения элементов.</p>
 * <p>Изменяет порядок элементов непосредственно в коллекции.</p>
 * <p>Для пустой коллекции команда не выполняет никаких действий.</p>
 */
public class Shuffle extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 1349L представляет собой уникальный номер версии для данного класса.
     */
    @Serial
    private static final long serialVersionUID = 1349L;

    /**
     * Конструктор команды shuffle.
     * Инициализирует команду с параметрами:
     * - имя команды: "shuffle"
     * - описание: перемешивание элементов коллекции
     * - флаг hasArgs: false (команда не требует дополнительных аргументов)
     */
    public Shuffle() {
        super("shuffle",
                "shuffle : перемешать элементы коллекции в случайном порядке",
                false);
    }
}