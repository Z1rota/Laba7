package org.example.commands;

import java.io.Serial;
import java.io.Serializable;

/**
 * Команда 'remove_at' - удаляет элемент коллекции по указанному индексу.
 * Наследует базовую функциональность от абстрактного класса Command
 * и реализует интерфейс Serializable для поддержки сериализации.
 *
 * <p>Команда требует обязательного аргумента - целочисленного индекса элемента.</p>
 * <p>При выполнении проверяет корректность указанного индекса (должен быть в пределах коллекции).</p>
 */
public class RemoveAt extends Command implements Serializable {

    /**
     * Уникальный идентификатор версии сериализации.
     * Обеспечивает корректную десериализацию объекта между разными версиями класса.
     * Значение 1345L представляет собой уникальный номер версии для этого класса.
     */
    @Serial
    private static final long serialVersionUID = 1345L;

    /**
     * Конструктор команды remove_at.
     * Инициализирует команду с параметрами:
     * - имя команды: "remove_at"
     * - описание: удаление элемента по указанному индексу
     * - флаг hasArgs: true (команда требует обязательного аргумента - индекса)
     */
    public RemoveAt() {
        super("remove_at",
                "remove_at index : удалить элемент, находящийся в " +
                        "заданной позиции коллекции (index)",
                true);
    }
}